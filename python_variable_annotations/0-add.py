#!/usr/bin/env python3
"""
Module 0-add
This module defines a function that adds two floating-point numbers.
"""


def add(a: float, b: float) -> float:
    """
    Add two float numbers and return the result as a float.

    Args:
        a (float): The first number to add.
        b (float): The second number to add.

    Returns:
        float: The sum of a and b.
    """
    return a + b
